    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        connectDatabase();
        String strID = tfldID.getText();
        String strName = tfldName.getText();
        String strTickets = tfldTickets.getText();
        String strQuery = "INSERT INTO "
                + "MovieList(movie_ID, movie_Name, movie_Tickets, movie_Reserved, movie_Showing)"
                + " VALUES("+strID+",'"+strName+"','"+strTickets+"','"+"0"+"','"+"N"+"')";
        if (tfldName.equals("")||tfldTickets.equals("")||flImage == null) {
            java.awt.Toolkit.getDefaultToolkit().beep();
        }
        else {
            try {
                pstmtMain = conMain.prepareStatement(strQuery);
                pstmtMain.execute();
                File flDestination = new File("src\\mwbs\\images\\movies\\"+tfldID.getText()+".jpg");
                FileUtils.copyFile(flImage, flDestination);
                new FrAdminManageMovie().setVisible(true);
                this.dispose();
            }
            catch (SQLException | IOException error) {
                System.out.println(error);
                java.awt.Toolkit.getDefaultToolkit().beep();
            }
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

