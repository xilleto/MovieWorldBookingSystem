    private int incrementID(){
        connectDatabase();
        String strID;
        int intID = 0;
        String strQuery = "SELECT "
                + "movie_ID "
                + "from MovieList "
                + "order by movie_ID";
        try{
            pstmtMain = conMain.prepareStatement(strQuery);
            rsMain = pstmtMain.executeQuery();
            while(rsMain.next()){
                strID = rsMain.getString("movie_ID");
                intID = Integer.parseInt(strID)+1;
            }
        }
        catch(SQLException error){
            System.out.println(error);
        }
        if(intID == 0){
            return 1000;
        }
        else{
            return intID;
        }
    }

